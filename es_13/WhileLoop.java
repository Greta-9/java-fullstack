
public class WhileLoop {
    public static void main(String[] args) {
        int i = 1; //dichiaro una variabile intero "i" e le assegno il valore 1
        while (i <= 10) {  //dichiaro che il ciclo while ripeta la variabile i finchè è minore o uguale a 10
            System.out.println(i);
            i++; //dichiaro che i aumenta di uno ad ogni ciclo
        }
    }
    
}
